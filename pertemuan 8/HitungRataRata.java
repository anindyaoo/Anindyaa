public class HitungRataRata {

    public static void main(String[] args) {
        // Menghitung rata-rata dua bilangan
        double nilai1 = 21;
        double nilai2 = 13;
        double rataRata1 = (nilai1 + nilai2) / 2;
        System.out.println("Rerata nilai " + nilai1 + " dan " + nilai2 + " adalah: " + rataRata1);

        // Menghitung rata-rata dua bilangan desimal
        double nilai3 = 19.3;
        double nilai4 = 39.5;
        double rataRata2 = (nilai3 + nilai4) / 2;
        System.out.println("Rerata nilai " + nilai3 + " dan " + nilai4 + " adalah: " + rataRata2);

        // Menghitung rata-rata tiga bilangan
        double nilai5 = 123;
        double nilai6 = 567;
        double nilai7 = 744;
        double rataRata3 = (nilai5 + nilai6 + nilai7) / 3;
        System.out.println("Rerata nilai " + nilai5 + ", " + nilai6 + ", dan " + nilai7 + " adalah: " + rataRata3);
    }
}
